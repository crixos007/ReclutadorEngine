CREATE TABLE HUMAN.HU_PANDORA_IDIOMA (
	ID_IDIOMA   NUMBER GENERATED ALWAYS AS IDENTITY (START WITH 1 INCREMENT BY 1),
	DESCRIPCION VARCHAR2(120) NOT NULL,
	STATUS      VARCHAR2(1) NOT NULL,
	PRIMARY KEY(ID_IDIOMA)
);


CREATE TABLE HUMAN.HU_PANDORA_COMPANIA (
	NUM_CIA         NUMBER GENERATED ALWAYS AS IDENTITY (START WITH 1 INCREMENT BY 1),
	NOMBRE          VARCHAR2(120) NOT NULL,
	RAZON_SOCIAL    VARCHAR2(120) NOT NULL,
	STATUS          VARCHAR2(1) NOT NULL,
	PRIMARY KEY(NUM_CIA)
);

CREATE TABLE HUMAN.HU_PANDORA_USUARIO (
	ID_USUARIO      NUMBER GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1),
	ID_IDIOMA       NUMBER NOT NULL,
	NUM_CIA         NUMBER NOT NULL,
	EMAIL           VARCHAR2(120) NOT NULL UNIQUE,
	PASSWORD        VARCHAR2(30) NOT NULL,
	NOMBRE          VARCHAR2(30) NOT NULL,
	APELLIDO_PAT    VARCHAR2(30) NOT NULL,
	APELLIDO_MAT    VARCHAR2(30) NOT NULL,
	RFC             VARCHAR2(30) NOT NULL,
	DIRECCION       VARCHAR2(240) NOT NULL,
	TELEFONO        VARCHAR2(12) NOT NULL,
	FOTOGRAFIA      CLOB,
	STATUS          VARCHAR2(1) NOT NULL,
	PRIMARY KEY(ID_USUARIO, ID_IDIOMA, NUM_CIA, EMAIL),
	CONSTRAINT FK_ID_IDIOMA
    FOREIGN KEY (ID_IDIOMA)
    REFERENCES HUMAN.HU_PANDORA_IDIOMA (ID_IDIOMA),
	CONSTRAINT FK_NUM_CIA
    FOREIGN KEY (NUM_CIA)
    REFERENCES HUMAN.HU_PANDORA_COMPANIA (NUM_CIA)
);

CREATE TABLE HUMAN.HU_PANDORA_PERFIL_PUESTO (
    ID_PERFIL_PUESTO NUMBER GENERATED ALWAYS AS IDENTITY (START WITH 1300 INCREMENT BY 1),    
    NUM_CIA          NUMBER NOT NULL,
    NOMBRE_PERFIL    VARCHAR2(120) NOT NULL,
    ALIAS_PERFIL     VARCHAR2(120) NOT NULL,     
    STATUS           VARCHAR2(1) NOT NULL,
	FECHA_MOV        DATE NOT NULL,
    PRIMARY KEY(ID_PERFIL_PUESTO)
);

CREATE TABLE HUMAN.HU_PANDORA_HASHTAG (
    ID_HASHTAG       NUMBER GENERATED ALWAYS AS IDENTITY (START WITH 1300 INCREMENT BY 1),    
    ID_PERFIL_PUESTO NUMBER NOT NULL,
    NOMBRE           VARCHAR2(120) NOT NULL,    
    STATUS           VARCHAR2(1) NOT NULL,
    PRIMARY KEY(ID_HASHTAG),
    CONSTRAINT FK_ID_PERFIL_PUESTO
    FOREIGN KEY (ID_PERFIL_PUESTO)
    REFERENCES HUMAN.HU_PANDORA_PERFIL_PUESTO (ID_PERFIL_PUESTO)
);

CREATE TABLE HUMAN.HU_PANDORA_PUBLICACION (
	ID_PUBLICACION     NUMBER GENERATED ALWAYS AS IDENTITY (START WITH 1300 INCREMENT BY 1),
    ID_PERFIL_PUESTO   NUMBER NOT NULL,        
    NOMBRE_PUBILCACION VARCHAR2(120) NOT NULL,
	NUM_VACANTES       NUMBER NOT NULL,
    FECHA_MOV          DATE NOT NULL,     
    STATUS             VARCHAR2(1) NOT NULL,
	ANALIZANDO         VARCHAR2(1) DEFAULT ON NULL 'N',
    PRIMARY KEY(ID_PUBLICACION), 
	CONSTRAINT FK_ID_PERFIL_PUESTO_PUB
    FOREIGN KEY (ID_PERFIL_PUESTO)
    REFERENCES HUMAN.HU_PANDORA_PERFIL_PUESTO (ID_PERFIL_PUESTO)
);

CREATE TABLE HUMAN.HU_PANDORA_HASHTAG_PUBLICACION (
    ID_HASHTAG     NUMBER GENERATED ALWAYS AS IDENTITY (START WITH 1300 INCREMENT BY 1),    
    ID_PUBLICACION NUMBER NOT NULL,
    NOMBRE         VARCHAR2(120) NOT NULL,    
    STATUS         VARCHAR2(1) NOT NULL,
    PRIMARY KEY(ID_HASHTAG),
    CONSTRAINT FK_ID_PUBLICACION
    FOREIGN KEY (ID_PUBLICACION)
    REFERENCES HUMAN.HU_PANDORA_PUBLICACION (ID_PUBLICACION)
);

CREATE TABLE HUMAN.HU_PANDORA_CANDIDATO (
    ID_CANDIDATO     NUMBER GENERATED ALWAYS AS IDENTITY (START WITH 1300 INCREMENT BY 1),
    NOMBRE_CANDIDATO VARCHAR2(120) NOT NULL,
	NOMBRE_CV        VARCHAR2(120) NOT NULL,
	FOTO             CLOB,
	STATUS           VARCHAR2(1) NOT NULL,
	ID_PUBLICACION   NUMBER,
	ORDEN            NUMBER,
	CV_SELECCIONADO  VARCHAR2(1) DEFAULT ON NULL 'N',
	ID_IMPORTACION   NUMBER,
    PRIMARY KEY(ID_CANDIDATO),
    CONSTRAINT FK_PUBLICACION
    FOREIGN KEY (ID_PUBLICACION)
    REFERENCES HUMAN.HU_PANDORA_PUBLICACION (ID_PUBLICACION)
);

CREATE TABLE HUMAN.HU_PANDORA_NOMBRES_VALIDOS (
    ID_NOMBRE_VAL  NUMBER GENERATED ALWAYS AS IDENTITY (START WITH 1300 INCREMENT BY 1),
    NOMBRE         VARCHAR2(50) NOT NULL UNIQUE,
    PRIMARY KEY(ID_NOMBRE_VAL)
);